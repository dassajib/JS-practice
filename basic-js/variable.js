// Variable is a thing where we can keep our info's.Basically variable has 3 three types. Number, String and boolean.Variable name should be meaningful and camel case.
var myName = "Sajib";
var number = 7;
var isItYou = true;
console.log(myName);
console.log(number);
console.log(isItYou);

// we can know variable's type bt using typeOf
console.log(typeof myName);
console.log(typeof number);
console.log(typeof isItYou);
var date = new Date;
console.log(date);
console.log(typeof date);

// We can update variable's info
var age = 17;
age += 1;
console.log(age);

var cost = 100;
cost -= 2;
console.log(cost);

