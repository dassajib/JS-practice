// a "While" Loop is used to repeat a specific block of code an unknown number of times.

// i will print all the numbers of 1 - 20.
var number = 1;
while (number <= 20) {
    console.log(number);
    number++;
}

// even numbers.
var numbers = 0;
while (numbers <= 10) {
    console.log(numbers);
    numbers = numbers + 2;
}

// Odd number.
var numberss = 1;
while (numberss <= 9) {
    console.log(numberss);
    numberss = numberss + 2;
}